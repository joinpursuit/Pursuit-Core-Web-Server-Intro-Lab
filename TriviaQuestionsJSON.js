const getRandomIndex = (range, tracker) => {
  let randomIndex = Math.floor(Math.random() * range);
  if (!tracker[randomIndex]) {
      tracker[randomIndex] = true;
      return randomIndex
  } else {
      return getRandomIndex(range, tracker)
  }
}

